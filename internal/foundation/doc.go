// Package foundation provides core methodology types and definitions for MoAI-ADK.
//
// This package contains the foundational building blocks used throughout
// the MoAI-ADK system, including:
//
//   - EARS (Easy Approach to Requirements Syntax) requirement patterns
//   - TRUST 5 quality framework (Tested, Readable, Understandable, Secured, Trackable)
